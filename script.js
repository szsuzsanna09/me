let currentSetIndex = 0;
let flippedCards = [];
let matched = 0;
let score = 0;
let timer;
let timeLeft = 60;
let gameActive = false;

const cardGrid = document.getElementById("card-grid");

function startGame() {
  document.getElementById("start-btn").style.display = "none";
  document.getElementById("game-area").style.display = "block";
  loadSet(currentSetIndex);
  startTimer();
}

function shuffle(array) {
  return array.sort(() => Math.random() - 0.5);
}

function startGame() {
  shuffle(vocabSets); // Randomize the order of sets
  currentSetIndex = 0;
  document.getElementById("times-up-message").style.display = "none";
  document.getElementById("start-btn").style.display = "none";
  document.getElementById("game-area").style.display = "block";
  loadSet(currentSetIndex);
  startTimer();
}

function startTimer() {
  clearInterval(timer);
  timeLeft = 60;
  gameActive = true;

  document.getElementById("times-up-message").style.display = "none"; // Hide old message
  document.getElementById("timer-label").textContent = `Time: ${timeLeft}`;

  timer = setInterval(() => {
    timeLeft--;
    document.getElementById("timer-label").textContent = `Time: ${timeLeft}`;
    if (timeLeft <= 0) {
      clearInterval(timer);
      gameActive = false;
      document.getElementById("times-up-message").style.display = "block"; // Show message
    }
  }, 1000);
}

function loadSet(index) {
  const set = vocabSets[index];

  // Update the UI labels here:
  document.getElementById("subtitle-label").textContent = set.subtitle;
  document.getElementById("cefr-label").textContent = `Level: ${set.level}`;
  document.getElementById("score-label").textContent = `Score: ${score}`;
  document.getElementById("timer-label").textContent = `Time: ${timeLeft}`;
  document.getElementById("times-up-message").style.display = "none";



  // Clear and render cards etc.
  cardGrid.innerHTML = "";
  flippedCards = [];
  matched = 0;

  const allCards = set.pairs.flatMap(pair => [
    { text: pair[0], id: pair.join('-') },
    { text: pair[1], id: pair.join('-') }
  ]);

  shuffle(allCards).forEach(card => {
    const cardDiv = document.createElement("div");
    cardDiv.className = "card";
    cardDiv.dataset.id = card.id;
    cardDiv.innerText = "";
    cardDiv.addEventListener("click", () => flipCard(cardDiv, card.text));
    cardGrid.appendChild(cardDiv);
  });

  startTimer(); // 🔁 Start/restart timer here
}

function flipCard(cardDiv, word) {
  if (!gameActive || flippedCards.length === 2 || cardDiv.classList.contains("flipped")) return;

  cardDiv.classList.add("flipped");
  cardDiv.innerText = word;
  flippedCards.push(cardDiv);

  if (flippedCards.length === 2) {
    const [first, second] = flippedCards;
    if (first.dataset.id === second.dataset.id) {
      score++;
      matched++;
      flippedCards = [];
      document.getElementById("score-label").textContent = `Score: ${score}`;

      const totalPairs = vocabSets[currentSetIndex].pairs.length;
      if (matched === totalPairs) {
        clearInterval(timer); // 🛑 stop the countdown
        gameActive = false;
        document.getElementById("times-up-message").innerHTML = `<strong>🎉 You matched all pairs!</strong>`;
        document.getElementById("times-up-message").style.display = "block";
      }

    } else {
      setTimeout(() => {
        first.classList.remove("flipped");
        second.classList.remove("flipped");
        first.innerText = "";
        second.innerText = "";
        flippedCards = [];
      }, 800);
    }
  }
}

document.getElementById("start-btn").addEventListener("click", startGame);

document.getElementById("next-btn").addEventListener("click", () => {
  if (currentSetIndex < vocabSets.length - 1) {
    currentSetIndex++;
    loadSet(currentSetIndex);
  }
});

document.getElementById("back-btn").addEventListener("click", () => {
  if (currentSetIndex > 0) {
    currentSetIndex--;
    loadSet(currentSetIndex);
  }
});

